DROP TABLE BUDS;
CREATE TABLE BUDS(

    BID     NVARCHAR2(10),  -- 아이디
    BPW     NVARCHAR2(10),  -- 비밀번호
    BNAME   NVARCHAR2(5),   -- 이름
    BBIRTH  DATE,           -- 생년월일
    BGENDER NVARCHAR2(2),   -- 성별
    BEMAIL  NVARCHAR2(20),  -- 이메일
    BPHONE  NVARCHAR2(13),  -- 전화번호
    ACCOUNTNUMBER NVARCHAR2(20) UNIQUE,              -- 계좌번호
    BALANCE NUMBER(15) DEFAULT 0             -- 잔액
);
SELECT * FROM BUDS;

